
function funCheck() {
    let checkBox = document.getElementById("check_1");
    let par = document.getElementsByClassName('container_item');
    if ( checkBox.checked ) {
        par[0].classList.add("gray");
        console.log(1);
    } else {
        par[0].classList.remove("gray");
        console.log(2);

    }
    

}
